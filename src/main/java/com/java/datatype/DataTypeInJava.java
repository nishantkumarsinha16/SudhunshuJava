package com.java.datatype;

public class DataTypeInJava {

	public static void main(String[] args) {

		int num = 50;
		float f = 50.0f;
		char c = 'c';
		double d = 50.0;
		boolean b = true;
		String s = "Nishant";

		System.out.println("Int: " + num);// 50
		System.out.println("float: " + f);// 50.0
		System.out.println("char: " + c);// c
		System.out.println("double: " + d);// 50.0
		System.out.println("boolean: " + b);// true
		System.out.println("String: " + s);// Nishant
		float result = num + f;
		System.out.println(result);

	}

}
